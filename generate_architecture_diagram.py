from graphviz import Digraph
import os

def create_main_architecture():
    dot = Digraph(comment='Shoplifting Detection System Architecture')
    dot.attr(rankdir='TB')
    dot.attr('node', shape='box', style='rounded')
    dot.node('A', 'Raw Video Data\n(MP4/AVI)')
    dot.node('B', 'Data Preparation\n(1_prepare_data.py)')
    dot.node('C', 'Frame Extraction\n(30 frames/video)')
    dot.node('D', 'Dataset Creation')
    dot.node('E', 'Training Data\n(80%)')
    dot.node('F', 'Validation Data\n(20%)')
    dot.node('G', 'Model Training\n(2_train_model.py)')
    dot.node('H', 'YOLOv8 Model')
    dot.node('H_caption', 'YOLOv8: Fine-tuned classifier\n(Normal vs Shoplifting)\nUsed for training & inference', shape='note', fontsize='10')
    dot.node('I', 'Testing Pipeline')
    dot.node('J', 'Pretrained Test\n(3_test_pretrained.py)')
    dot.node('K', 'Custom Test\n(4_test_custom.py)')
    dot.node('L', 'Results & Analytics')
    dot.edge('A', 'B')
    dot.edge('B', 'C')
    dot.edge('C', 'D')
    dot.edge('D', 'E')
    dot.edge('D', 'F')
    dot.edge('E', 'G')
    dot.edge('F', 'G')
    dot.edge('G', 'H')
    dot.edge('H', 'I')
    dot.edge('I', 'J')
    dot.edge('I', 'K')
    dot.edge('J', 'L')
    dot.edge('K', 'L')
    dot.edge('H', 'H_caption', style='dashed', arrowhead='none')
    dot.render('architecture/main_architecture', format='png', cleanup=True)

def create_data_preparation_diagram():
    dot = Digraph(comment='Data Preparation Pipeline')
    dot.attr(rankdir='LR')
    dot.attr('node', shape='box', style='rounded')
    dot.node('A', 'Input Videos')
    dot.node('B', 'Frame Extractor')
    dot.node('C', 'Train-Val Splitter')
    with dot.subgraph(name='cluster_0') as c:
        c.attr(label='Dataset Structure')
        c.node('D1', 'Train/Normal')
        c.node('D2', 'Train/Shoplifting')
        c.node('D3', 'Val/Normal')
        c.node('D4', 'Val/Shoplifting')
    dot.edge('A', 'B')
    dot.edge('B', 'C')
    dot.edge('C', 'D1')
    dot.edge('C', 'D2')
    dot.edge('C', 'D3')
    dot.edge('C', 'D4')
    dot.render('architecture/data_preparation', format='png', cleanup=True)

