def verify_graphviz():
    import shutil
    if not shutil.which('dot'):
        print("ERROR: Graphviz is not found in PATH!")
        print("\nPlease ensure:")
        print("1. Graphviz is installed (you can install it using 'winget install graphviz')")
        print("2. Add Graphviz to PATH:")
        print("   - Usually located at: C:\\Program Files\\Graphviz\\bin")
        print("   - Add this path to your system's PATH environment variable")
        print("\nAfter installing, you may need to restart your terminal/VS Code")
        return False
    return True

def main():
    if not verify_graphviz():
        return
    os.makedirs('architecture', exist_ok=True)
    print("Generating architecture diagrams...")
    create_main_architecture()
    create_data_preparation_diagram()
    create_model_training_diagram()
    create_testing_pipeline_diagram()
    print("Architecture diagrams have been generated in the 'architecture' folder:")
    print("1. main_architecture.png - Overall system architecture")
    print("2. data_preparation.png - Data preparation pipeline")
    print("3. model_training.png - Model training pipeline")
    print("4. testing_pipeline.png - Testing pipeline")

if __name__ == "__main__":
    main()
